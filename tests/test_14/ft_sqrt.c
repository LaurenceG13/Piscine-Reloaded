int		ft_sqrt(int nb)
{
	int		sqrt;

	sqrt = 1;
	if (nb > 0)
	{
		while (sqrt < nb / 2)
		{
			if (sqrt * sqrt - nb == 0)
				return (sqrt);
			sqrt++;
		}
		return (0);
	}
	return (0);
}
