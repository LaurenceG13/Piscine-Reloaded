int		ft_atoi(char *str)
{
	int		i;
	int		nb;
	int		s;

	nb = 0;
	i = 0;
	s = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
			|| str[i] == '\f' || str[i] == '\r')
	{
		i++;
	}
	if (str[i] == 45)
		s = (-1);
	if (str[i] == 43 || str[i] == 45)
		i++;
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb = 10 * nb + (str[i] - '0');
		i++;
	}
	return (nb * s);
}
